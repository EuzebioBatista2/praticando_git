<?php

  // %s string
  $name = "Euzebio";
  printf("Meu nome é: %s <br>", $name);

  // %d digit/integer
  $number_1 = 10;
  $number_2 = 15;
  printf("O valor_1 é: %d e o valor_2 é: %d <br>", $number_1, $number_2);

  // $f or $.2f float
  $number_float = 3.14456456;
  printf("O valor com 2 casas decimais é: %.2f <br>", $number_float);